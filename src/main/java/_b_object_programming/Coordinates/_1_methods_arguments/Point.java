package _b_object_programming.Coordinates._1_methods_arguments;

public class Point {
    int coordinatesX;
    int coordinatesY;

    void setX(int x) {
        coordinatesX = x;
    }

    void setY(int y) {
        coordinatesY = y;
    }

    int returnX() {
        return coordinatesX;
    }

    int returnY() {
        return coordinatesY;
    }
}