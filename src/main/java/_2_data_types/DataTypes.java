package _2_data_types;

/** practice of utilizing
 * different data types in JAVA
 */

public class DataTypes{
    public static void main(String[] args) {
        System.out.println("A printout of exemplary data types: \n");
        System.out.println('a'+'A'); // char
        System.out.println(1+4);  // f.ex. byte
        System.out.println(1.0+2.0); // float
        System.out.println("quotation mark \""); // \"
        System.out.println("apostrophe \'");  // \'
        System.out.println("backslash \\");  // \\
        System.out.println("new line \n");  // \n
        System.out.println("Wynik to: "+1+2+3);  // dodawanie od lewej do prawej
        System.out.println(1+2+3+" to wynik");
    }
}