package _4_math_and_logic_operators;

public class Operators {
    public static void main(String[] args) {
        int a = 17;
        int b = 4;
        int c = a + b;  // 21
        System.out.println(c);
        c = a - b;  // 13
        System.out.println(c);
        c = a * b;  // 68
        System.out.println(c);
        c = a / b;  // 4 cause 4 * 4 = 16 and remaining rest = 1;
        System.out.println(c);
        c = a % b;
        System.out.println(c);
    }
}
